import SwiftUI

struct NogiColor {
    static let red: Color = Color(.sRGB, red: 1, green: 0, blue: 0)
    static let yellow: Color = Color.yellow
    static let cyan: Color = Color(UIColor.cyan)
    static let green: Color = Color(.sRGB, red: 0, green: 1, blue: 0)
    static let blue: Color = Color(.sRGB, red: 0, green: 0, blue: 1)
    static let pink: Color = Color(.sRGB, red: 1, green: 0.4, blue: 0.7)
    static let purple: Color = Color(.sRGB, red: 0.5, green: 0, blue: 0.5)
    static let white: Color = Color.white
    static let black: Color = Color.black
    static let orange: Color = Color.orange
    static let chartreuse: Color = Color.init(red: 0.5, green: 1, blue: 0)
}
